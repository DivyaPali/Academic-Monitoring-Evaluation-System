
public class Lp1t {

	    private int rollno, marks;
	    
	    public Lp1t() {
			super();
		}

	    public Lp1t(int rollno, int marks) {
	    	super();
	        this.rollno = rollno;
	        this.marks = marks;

	    }
	    
	   
	    public int getRollno() {
	        return rollno;
	    }

	    
	    public void setRollno(int rollno) {
	    	this.rollno = rollno;
	    }
	    
	    public int getMarks() {
	        return marks;
	    }

	    
	    public void setMarks(int marks) {
	    	 this.marks = marks;
	    }
	    
	   
	    
}