
public class Lpft {

	    private int rollno, marks;
	    
	    public Lpft() {
			super();
		}

	    public Lpft(int rollno, int marks) {
	    	super();
	        this.rollno = rollno;
	        this.marks = marks;

	    }
	    
	   
	    public int getRollno() {
	        return rollno;
	    }

	    
	    public void setRollno(int rollno) {
	    	this.rollno = rollno;
	    }
	    
	    public int getMarks() {
	        return marks;
	    }

	    
	    public void setmarks(int marks) {
	    	 this.marks = marks;
	    }
	    
	   
	    
}